
use serde::{Deserialize, Serialize};

#[derive(Serialize, Deserialize, Clone, Debug, Default, PartialEq)]
pub struct Trade {
	/// MsgType = DC
	#[serde(flatten)]
	pub standard_message_header: super::super::standard_message_header::StandardMessageHeader,
	/// ApplicationSequenceControl
	#[serde(flatten)]
	pub application_sequence_control: Option<super::super::application_sequence_control::ApplicationSequenceControl>,
	/// <p>Trade match identifier assigned in the matching engine and used to correlate a cleared trade with a match event. Should be
	/// common for all trades included in a match event. Conditionally required for trades reported from a matching platform.
	/// </p>
	#[serde(rename = "880")]
	pub trd_match_id: String,
	/// MatchType
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "574")]
	pub match_type: Option<MatchType>,
	/// Identifies the type of trade. Can indicate new submissions, addendums, cancellations, etc.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "856")]
	pub trade_report_type: Option<TradeReportType>,
	/// The business date for which the trade is expected to be cleared
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "715")]
	pub clearing_business_date: Option<fix_common::LocalMktDate>,
	/// TrdType
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "828")]
	pub trd_type: Option<TrdType>,
	/// TrdSubType
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "829")]
	pub trd_sub_type: Option<TrdSubType>,
	/// Used when reporting other than current day trades.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "75")]
	pub trade_date: Option<fix_common::LocalMktDate>,
	/// MarketID
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "1301")]
	pub market_id: Option<String>,
	/// MarketSegmentID
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "1300")]
	pub market_segment_id: Option<String>,
	/// TradingSessionID
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "336")]
	pub trading_session_id: Option<TradingSessionID>,
	/// TradingSessionSubID
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "625")]
	pub trading_session_sub_id: Option<TradingSessionSubID>,
	/// Identifies the type of venue where a trade was executed
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "1430")]
	pub venue_type: Option<VenueType>,
	/// Timestamp of the trade or match event. For off-exchange trades the time at which the deal was negotiated or matched by the
	/// exchange. This timestamp will be the same on all the trades and will not change when a trade is modified.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "1888")]
	pub trade_match_timestamp: Option<fix_common::UTCTimestamp>,
	/// Indicates time of the transaction. Used after initial submission to indicate time of addendum, cancellation, etc. Time of
	/// the match event or transaction that resulted in this match report.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "60")]
	pub transact_time: Option<fix_common::UTCTimestamp>,
	/// Type of report if multileg instrument. Differentiates match events involving complex instruments(MultiLegReportingType(442)=3(multi-leg
	/// security)) from those only involving simple instruments (MultiLegReportingType(442)= 1, 2) should not be used.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "442")]
	pub multi_leg_reporting_type: Option<MultiLegReportingType>,
	/// Match sides per instrument. This is conditionally required when submitting a new trade, e.g. TradeReportType = Submit(0).
	#[serde(flatten)]
	pub instrmt_match_side_grp: Option<super::super::instrmt_match_side_grp::InstrmtMatchSideGrp>,
	/// Standard Message Trailer
	#[serde(flatten)]
	pub standard_message_trailer: super::super::standard_message_trailer::StandardMessageTrailer,
}

