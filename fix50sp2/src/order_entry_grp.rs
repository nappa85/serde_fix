
use serde::{Deserialize, Serialize};

#[derive(Serialize, Deserialize, Clone, Debug, Default, PartialEq)]
pub struct OrderEntryGrp {
	/// NoOrderEntries
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "2428")]
	pub order_entries: Option<crate::entities::RepeatingValues<OrderEntrie>>,
}

#[derive(Serialize, Deserialize, Clone, Debug, Default, PartialEq)]
pub struct OrderEntrie {
	/// Required if NoOrderEntries(2428) &gt; 0.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "2429")]
	pub order_entry_action: Option<OrderEntryAction>,
	/// Unique order entry identification across all entries of a single message. Conditionally required when neither ClOrdID(11)
	/// nor OrderID(37) is provided.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(deserialize_with = "crate::entities::workarounds::from_opt_str")]// https://github.com/serde-rs/serde/issues/1183
	#[serde(default)]
	#[serde(rename = "2430")]
	pub order_entry_id: Option<i32>,
	/// Conditionally required when neither OrderEntryID(2430) nor OrderID(37) is provided.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "11")]
	pub cl_ord_id: Option<String>,
	/// Conditionally required when OrderEntryAction(2429) is not "1" (Add), ClOrdID(11) was provided in original order, and message-chaining
	/// model is used.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "41")]
	pub orig_cl_ord_id: Option<String>,
	/// Conditionally required when OrderEntryAction(2429) is not "1" (Add) and neither OrderEntryID(2430) nor ClOrdID(11) is provided.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "37")]
	pub order_id: Option<String>,
	/// Conditionally required when OrderEntryAction (2429) = 1 (Add) or 2 (Modify). Only a subset of OrdType(40) values permitted
	/// that do not require additional pricing fields other than Price(44) field.
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "40")]
	pub ord_type: Option<OrdType>,
	/// Conditionally required when OrdType(40) = 2 (Limit)
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(deserialize_with = "crate::entities::workarounds::from_opt_str")]// https://github.com/serde-rs/serde/issues/1183
	#[serde(default)]
	#[serde(rename = "44")]
	pub price: Option<f64>,
	/// Conditionally required when OrderEntryAction(2429) = 1 (Add) or 2 (Modify)
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "54")]
	pub side: Option<Side>,
	/// Only subset of values permitted that do not require additional fields
	#[serde(skip_serializing_if = "Option::is_none")]
	#[serde(rename = "59")]
	pub time_in_force: Option<TimeInForce>,
}

#[derive(Serialize, Deserialize, Clone, Debug, PartialEq)]
pub enum OrderEntryAction {
	/// Add
	#[serde(rename = "1")]
	Add,
	/// Modify
	#[serde(rename = "2")]
	Modify,
	/// Delete / Cancel
	#[serde(rename = "3")]
	DeleteCancel,
	/// Suspend
	#[serde(rename = "4")]
	Suspend,
	/// Release
	#[serde(rename = "5")]
	Release,
}

#[derive(Serialize, Deserialize, Clone, Debug, PartialEq)]
